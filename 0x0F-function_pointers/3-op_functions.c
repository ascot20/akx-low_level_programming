#include "3-calc.h"
#include <stdio.h>
/**
 * op_add-returns the sum
 * @a:int variable
 * @b:int variable
 * Return:return int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub-returns the subtraction
 * @a:int variable
 * @b:int variable
 * Return:return int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul-returns the multiplication
 * @a:int variable
 * @b:int variable
 * Return:return int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div-returns the division
 * @a:int variable
 * @b:int variable
 * Return:return int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod-returns the remainder
 * @a:int variable
 * @b:int variable
 * Return:return int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
